"""
BS24 Benchmark: Fortran vs Python
==================================
Reads the Fortran CSV output, runs the same scenarios through the Python
implementation, and compares ln(SA) and sigma.

Usage:
    python benchmark_bs24.py

Requires:
    - bs24_fortran_output.csv  (generated by bs24_run)
    - bayless_somerville_2024.py  (your Python implementation)

Output:
    - benchmark_bs24_results.csv   (all comparisons)
    - benchmark_bs24_plot.png      (residual plots)
    - prints a pass/fail summary to screen
"""
import sys
import os
import numpy as np
import pandas as pd
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

# --- Import the Python GMM ---
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from bayless_somerville_2024 import (
    BaylessSomerville2024Cratonic,
    BaylessSomerville2024NonCratonic,
)
from openquake.hazardlib.imt import PGA, SA

# Unit conversion: Fortran outputs ln(cm/s/s), Python outputs ln(g)
# 1 g = 980.665 cm/s/s  →  ln(980.665) = 6.888...
# The Fortran hardcodes +6.89, so we use the same value
LN_G_TO_CMSS = 6.89
TOLERANCE = 0.001   # acceptable absolute difference in ln units (~0.1%)

# ---------------------------------------------------------------------------
# Helper: run Python GMM for one scenario, return ln(g)
# ---------------------------------------------------------------------------
def run_python(gsim, mag, rrup, vs30, ztor, period):
    ctx = np.recarray(1, dtype=[
        ('mag', float), ('rrup', float), ('rjb', float),
        ('rx',  float), ('ztor', float), ('dip', float),
        ('rake',float), ('width',float), ('vs30', float),
        ('z1pt0',float), ('ry0', float),
    ])
    ctx.mag[:]   = mag
    ctx.rrup[:]  = rrup
    ctx.rjb[:]   = rrup
    ctx.rx[:]    = -50.0     # footwall — same as Fortran driver
    ctx.ztor[:]  = ztor
    ctx.dip[:]   = 45.0
    ctx.rake[:]  = 90.0
    ctx.width[:] = 15.0
    ctx.vs30[:]  = vs30
    ctx.z1pt0[:] = 0.0413   # same as Fortran z10_rock default
    ctx.ry0[:]   = 0.0

    imt = PGA() if period == 0.0 else SA(period)
    mean = np.zeros((1, 1))
    sig  = np.zeros((1, 1))
    tau  = np.zeros((1, 1))
    phi  = np.zeros((1, 1))
    gsim.compute(ctx, [imt], mean, sig, tau, phi)
    return float(mean[0, 0]), float(sig[0, 0])

# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
print("Reading Fortran output...")
df = pd.read_csv('bs24_fortran_output.csv', skipinitialspace=True)
print(f"  {len(df)} test cases loaded")

gsim_c  = BaylessSomerville2024Cratonic()
gsim_nc = BaylessSomerville2024NonCratonic()

results = []
print("Running Python GMM for each scenario...")

for _, row in df.iterrows():
    gsim = gsim_c if row['CratFlag'] == 1 else gsim_nc

    # Python returns ln(g); Fortran returns ln(cm/s/s)
    py_lng, py_sig = run_python(
        gsim, row['mag'], row['rrup'], row['vs30'],
        row['ztor'], row['period']
    )

    # Convert Fortran to ln(g) for comparison
    fort_lng = row['lnY_cmss'] - LN_G_TO_CMSS
    fort_sig = row['sigma']

    diff_mean  = py_lng  - fort_lng
    diff_sigma = py_sig  - fort_sig

    results.append({
        'CratFlag': int(row['CratFlag']),
        'mag':      row['mag'],
        'rrup':     row['rrup'],
        'vs30':     row['vs30'],
        'ztor':     row['ztor'],
        'period':   row['period'],
        'fort_lng': fort_lng,
        'py_lng':   py_lng,
        'diff_mean':  diff_mean,
        'fort_sig': fort_sig,
        'py_sig':   py_sig,
        'diff_sigma': diff_sigma,
        'pass_mean':  abs(diff_mean)  <= TOLERANCE,
        'pass_sigma': abs(diff_sigma) <= TOLERANCE,
    })

results = pd.DataFrame(results)

# ---------------------------------------------------------------------------
# Summary
# ---------------------------------------------------------------------------
n_total      = len(results)
n_pass_mean  = results['pass_mean'].sum()
n_pass_sigma = results['pass_sigma'].sum()
n_fail_mean  = n_total - n_pass_mean
n_fail_sigma = n_total - n_pass_sigma

print(f"\n{'='*60}")
print(f"BENCHMARK RESULTS  (tolerance = {TOLERANCE} ln units)")
print(f"{'='*60}")
print(f"Total test cases : {n_total}")
print(f"\nMedian (mean SA):")
print(f"  PASS : {n_pass_mean}/{n_total}  ({100*n_pass_mean/n_total:.1f}%)")
print(f"  FAIL : {n_fail_mean}")
print(f"  Max |diff| : {results['diff_mean'].abs().max():.6f} ln units")
print(f"  Mean diff  : {results['diff_mean'].mean():.6f} ln units")
print(f"\nSigma:")
print(f"  PASS : {n_pass_sigma}/{n_total}  ({100*n_pass_sigma/n_total:.1f}%)")
print(f"  FAIL : {n_fail_sigma}")
print(f"  Max |diff| : {results['diff_sigma'].abs().max():.6f} ln units")
print(f"{'='*60}")

# Show worst cases if any failures
if n_fail_mean > 0:
    print("\nWORST mean differences:")
    worst = results.nlargest(5, 'diff_mean')[
        ['CratFlag','mag','rrup','vs30','ztor','period',
         'fort_lng','py_lng','diff_mean']]
    print(worst.to_string(index=False))

# ---------------------------------------------------------------------------
# Save CSV
# ---------------------------------------------------------------------------
out_csv = 'benchmark_bs24_results.csv'
results.to_csv(out_csv, index=False, float_format='%.6f')
print(f"\nFull results saved to {out_csv}")

# ---------------------------------------------------------------------------
# Plot
# ---------------------------------------------------------------------------
fig, axes = plt.subplots(2, 2, figsize=(13, 10))
fig.suptitle('BS24 Benchmark: Python vs Fortran\n'
             f'(tolerance = {TOLERANCE} ln units)', fontsize=12)

periods_sorted = sorted(results['period'].unique())
colors = plt.cm.tab10(np.linspace(0, 1, len(periods_sorted)))
period_color = {p: c for p, c in zip(periods_sorted, colors)}

# Panel 1: diff_mean vs period (coloured by Cratonic/NonCratonic)
ax = axes[0, 0]
for crat, label, marker in [(1, 'Cratonic', 'o'), (0, 'NonCratonic', 's')]:
    sub = results[results['CratFlag'] == crat]
    ax.scatter(sub['period'], sub['diff_mean'],
               alpha=0.5, s=15, marker=marker, label=label)
ax.axhline(0, color='k', lw=0.8)
ax.axhline( TOLERANCE, color='r', lw=0.8, ls='--', label=f'±{TOLERANCE}')
ax.axhline(-TOLERANCE, color='r', lw=0.8, ls='--')
ax.set_xlabel('Period (s)')
ax.set_ylabel('Python − Fortran  [ln(g)]')
ax.set_title('Mean SA residuals vs Period')
ax.legend(fontsize=8)
ax.set_xscale('log')
ax.grid(True, alpha=0.3)

# Panel 2: diff_mean vs Rrup
ax = axes[0, 1]
for p in periods_sorted:
    sub = results[results['period'] == p]
    ax.scatter(sub['rrup'], sub['diff_mean'],
               alpha=0.5, s=15, color=period_color[p],
               label=f'T={p}s')
ax.axhline(0, color='k', lw=0.8)
ax.axhline( TOLERANCE, color='r', lw=0.8, ls='--')
ax.axhline(-TOLERANCE, color='r', lw=0.8, ls='--')
ax.set_xlabel('Rrup (km)')
ax.set_ylabel('Python − Fortran  [ln(g)]')
ax.set_title('Mean SA residuals vs Distance')
ax.set_xscale('log')
ax.grid(True, alpha=0.3)
ax.legend(fontsize=7, ncol=2)

# Panel 3: diff_sigma vs period
ax = axes[1, 0]
for crat, label, marker in [(1, 'Cratonic', 'o'), (0, 'NonCratonic', 's')]:
    sub = results[results['CratFlag'] == crat]
    ax.scatter(sub['period'], sub['diff_sigma'],
               alpha=0.5, s=15, marker=marker, label=label)
ax.axhline(0, color='k', lw=0.8)
ax.axhline( TOLERANCE, color='r', lw=0.8, ls='--', label=f'±{TOLERANCE}')
ax.axhline(-TOLERANCE, color='r', lw=0.8, ls='--')
ax.set_xlabel('Period (s)')
ax.set_ylabel('Python − Fortran  [ln units]')
ax.set_title('Sigma residuals vs Period')
ax.legend(fontsize=8)
ax.set_xscale('log')
ax.grid(True, alpha=0.3)

# Panel 4: histogram of diff_mean
ax = axes[1, 1]
ax.hist(results['diff_mean'], bins=40, color='steelblue',
        edgecolor='white', alpha=0.8)
ax.axvline(0, color='k', lw=1)
ax.axvline( TOLERANCE, color='r', lw=1, ls='--', label=f'±{TOLERANCE}')
ax.axvline(-TOLERANCE, color='r', lw=1, ls='--')
ax.set_xlabel('Python − Fortran  [ln(g)]')
ax.set_ylabel('Count')
ax.set_title(f'Distribution of mean SA differences\n'
             f'max={results["diff_mean"].abs().max():.5f}  '
             f'mean={results["diff_mean"].mean():.5f}')
ax.legend(fontsize=8)
ax.grid(True, alpha=0.3)

plt.tight_layout()
out_png = 'benchmark_bs24_plot.png'
plt.savefig(out_png, dpi=150, bbox_inches='tight', facecolor='white')
print(f"Plot saved to {out_png}")
