"""
BS24 Response Spectra from Fortran Output
==========================================
Reads bs24_fortran_output.csv and plots response spectra in the
same style as Figure D-16 in the BS24 appendix:
  - Two panels: Rrup = 10 km (left) and Rrup = 100 km (right)
  - Five magnitudes in colour
  - Cratonic = solid lines, NonCratonic = dotted lines
  - Vs30 = 760 m/s, Ztor = 0 km, footwall

Usage:
    python plot_bs24_fortran_spectra.py

Requires:
    - bs24_fortran_output.csv  (generated by ./bs24_run)
    - matplotlib, pandas, numpy
"""
import numpy as np
import pandas as pd
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import matplotlib.ticker as ticker
from matplotlib.lines import Line2D

# -----------------------------------------------------------------------
# Load Fortran output
# -----------------------------------------------------------------------
df = pd.read_csv('bs24_fortran_output.csv', skipinitialspace=True)

# Convert ln(cm/s/s) to g
LN_G_TO_CMSS = 6.89
df['SA_g'] = np.exp(df['lnY_cmss'] - LN_G_TO_CMSS)

# Keep only the periods we want for a clean spectrum (exclude PGA=0)
df = df[df['period'] > 0].copy()

# -----------------------------------------------------------------------
# Plot settings — same as bs24_response_spectra.png
# -----------------------------------------------------------------------
MAGNITUDES = [4.0, 5.0, 6.0, 7.0, 8.0]
RRUP_LEFT  = 15.0
RRUP_RIGHT = 150.0
VS30       = 760.0
ZTOR       = 0.0

MAG_COLORS = {
    4.0: '#1f77b4',   # blue
    5.0: '#ff7f0e',   # orange
    6.0: '#2ca02c',   # green
    7.0: '#d62728',   # red
    8.0: '#9467bd',   # purple
}

fig, axes = plt.subplots(1, 2, figsize=(13, 6), sharey=False)
fig.patch.set_facecolor('white')

panel_info = [
    (axes[0], RRUP_LEFT,  f'(a) R$_{{rup}}$ = {RRUP_LEFT:.0f} km'),
    (axes[1], RRUP_RIGHT, f'(b) R$_{{rup}}$ = {RRUP_RIGHT:.0f} km'),
]

for ax, rrup, panel_label in panel_info:
    for mag in MAGNITUDES:
        color = MAG_COLORS[mag]

        # Cratonic — solid line
        sub_c = df[
            (df['CratFlag'] == 1) &
            (np.isclose(df['mag'],  mag,  atol=0.01)) &
            (np.isclose(df['rrup'], rrup, atol=0.1)) &
            (np.isclose(df['vs30'], VS30, atol=1.)) &
            (np.isclose(df['ztor'], ZTOR, atol=0.1))
        ].sort_values('period')

        # NonCratonic — dotted line
        sub_nc = df[
            (df['CratFlag'] == 0) &
            (np.isclose(df['mag'],  mag,  atol=0.01)) &
            (np.isclose(df['rrup'], rrup, atol=0.1)) &
            (np.isclose(df['vs30'], VS30, atol=1.)) &
            (np.isclose(df['ztor'], ZTOR, atol=0.1))
        ].sort_values('period')

        if not sub_c.empty:
            ax.loglog(sub_c['period'], sub_c['SA_g'],
                      color=color, lw=2.0, linestyle=':')

        if not sub_nc.empty:
            ax.loglog(sub_nc['period'], sub_nc['SA_g'],
                      color=color, lw=2.0, linestyle='-')

    # Formatting
    ax.set_xlabel('Period (s)', fontsize=12)
    if ax is axes[0]:
        ax.set_ylabel('RotD50 SA (g)', fontsize=12)
    ax.set_title(panel_label, fontsize=12, loc='left', pad=8)
    ax.set_xlim([0.01, 10])
    ax.set_ylim([1e-5, 2e0])
    ax.grid(True, which='both', alpha=0.3, lw=0.5)
    ax.tick_params(axis='both', which='both', labelsize=10)
    ax.xaxis.set_major_formatter(
        ticker.FuncFormatter(lambda x, _: f'{x:g}'))
    ax.set_xticks([0.01, 0.1, 1.0, 10.0])

    # Scenario text box
    textstr = (f'Z$_{{tor}}$ = {ZTOR:.1f} km\n'
               f'V$_{{S30}}$ = {VS30:.0f} m/s\n'
               f'Footwall\nFortran output')
    ax.text(0.97, 0.97, textstr, transform=ax.transAxes,
            fontsize=9, va='top', ha='right',
            bbox=dict(boxstyle='round,pad=0.3', facecolor='lightyellow',
                      edgecolor='gray', alpha=0.8))

# -----------------------------------------------------------------------
# Legend
# -----------------------------------------------------------------------
mag_handles = [
    Line2D([0], [0], color=MAG_COLORS[m], lw=2, label=f'M {m:.1f}')
    for m in MAGNITUDES
]
style_handles = [
    Line2D([0], [0], color='k', lw=2, linestyle=':',  label='Cratonic'),
    Line2D([0], [0], color='k', lw=2, linestyle='-', label='NonCratonic'),
]
axes[0].legend(handles=mag_handles + style_handles,
               fontsize=9, loc='lower left', framealpha=0.9)

# -----------------------------------------------------------------------
# Title and save
# -----------------------------------------------------------------------
#fig.suptitle(
#    'Bayless and Somerville (2024) — Response Spectra from Fortran\n'
#    'Cratonic (dotted) vs NonCratonic (solid), Vs30 = 760 m/s, '
#    'Ztor = 0 km, Footwall',
#    fontsize=11, y=1.01
#)
plt.tight_layout()
plt.savefig('bs24_fortran_spectra.png', dpi=150,
            bbox_inches='tight', facecolor='white')
print('Saved: bs24_fortran_spectra.png')
